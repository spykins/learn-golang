package main
import "fmt"

func main() {
  name, age, email := "Wale", 20, "wale@wale.com"


  fmt.Printf("The value of name is : %s\n", name)
  fmt.Printf("The type of name is : %T\n", name)
  
  fmt.Printf("\n The value of age is %d\n", age)
  fmt.Printf("The type of age is %T\n", age)

  fmt.Printf("\nThe value of email is : %s\n", email)
 fmt.Printf("The type of email is : %T\n", email)

}
